	ImGuiContext::ImGuiContext(const class Window& win, const class Renderer& renderer) : _renderer(renderer)
	{
		IMGUI_CHECKVERSION();
    	ImGui::CreateContext();
		ImGuiIO& io = ImGui::GetIO(); (void)io;
		io.ConfigFlags |= ImGuiConfigFlags_NavEnableKeyboard;
		io.ConfigFlags |= ImGuiConfigFlags_DockingEnable;
		
		setDarkTheme();

		ImGui_ImplSDL2_InitForSDLRenderer(win.getNativeWindow(), renderer.getNativeRenderer());
		ImGui_ImplSDLRenderer2_Init(renderer.getNativeRenderer());

		_is_init = true;
	}

	bool ImGuiContext::checkEvents() noexcept
	{
		SDL_Event event;
		while(SDL_PollEvent(&event))
		{
			ImGui_ImplSDL2_ProcessEvent(&event);
			if(event.type == SDL_QUIT)
				return false;
			if(event.type == SDL_WINDOWEVENT && event.window.event == SDL_WINDOWEVENT_CLOSE)
				return false;
		}
		return true;
	}

	void ImGuiContext::beginFrame() noexcept
	{
		ImGui_ImplSDLRenderer2_NewFrame();
		ImGui_ImplSDL2_NewFrame();
		ImGui::NewFrame();
	}

	void ImGuiContext::endFrame() noexcept
	{
		ImGuiIO& io = ImGui::GetIO();
		ImGui::Render();
		SDL_RenderSetScale(_renderer.getNativeRenderer(), io.DisplayFramebufferScale.x, io.DisplayFramebufferScale.y);
		SDL_SetRenderDrawColor(_renderer.getNativeRenderer(), 0, 0, 0, 255);
		SDL_RenderClear(_renderer.getNativeRenderer());
		ImGui_ImplSDLRenderer2_RenderDrawData(ImGui::GetDrawData());
		SDL_RenderPresent(_renderer.getNativeRenderer());
	}

	void ImGuiContext::destroy() noexcept
	{
		if(!_is_init)
			return;
		ImGui_ImplSDLRenderer2_Shutdown();
		ImGui_ImplSDL2_Shutdown();
		ImGui::DestroyContext();
		_is_init = false;
	}

	ImGuiContext::~ImGuiContext()
	{
		destroy();
	}

